let value; // value라는 변수를 선언한다.

value= 10; //선언한 변수 value에 10의 값을 할당한다.

console.log(`할당된 변수 vaule 의 값은 : ${value}`);
value = 20; //value변수에 다른 값을 재 할당한다.

console.log(`재 할당된 변수 vaule 의 값은 : ${value}`);

const pi = 3.14; //pi라는 변수를 const 로 선언한다.
//할당이 동시에 이루어지지 않으면 오류가 나타난다.

console.log(`할당된 변수 pi 의 값은 : ${pi}`);